//Print alle ASCII klein char
public class ASCII_Klein {
    
	public static void main(String[] args) {
        
        //ASCII 65-90
        
        int i;
        char c;
 
        for(i=97;i<=122;i++) {
 
           c =(char)i; // int to char
         
           System.out.println(i+"  =  "+c); //Print
        }
 
 
     }

}
